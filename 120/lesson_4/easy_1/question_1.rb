# Question 1.
# Which of the following are objects in Ruby. If they are objects, how can you
# find out what class they belong to?

# 1 ) true
# 2 ) "hello"
# 3 ) [1, 2, 3, "happy days"]
# 4) 142

# Answer: They are all objects. To see the class, use the method .class

puts true.class                      # True class
puts "hello".class                   #string Class
puts [1, 2, 3, "happy days"].class   #Array class
puts 142.Class                       #Fixnum class